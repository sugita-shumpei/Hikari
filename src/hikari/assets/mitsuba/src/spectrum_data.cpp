#include <sstream>

hikari::Bool hikari::MitsubaSpectrumData::load(const String &filename)
{
    std::ifstream file(filename);
    if (file.is_open())
    {
        wavelengths.clear();
        values.clear();
        std::string sentence;
        while (std::getline(file, sentence, '\n'))
        {
            if (sentence[0] == '#')
            {
                continue;
            }
            std::stringstream ss(sentence);
            F32 f0, f1;
            ss >> f0 >> f1;
            wavelengths.push_back(f0);
            values.push_back(f1);
        }
        file.close();
        return true;
    }

    return false;
}

